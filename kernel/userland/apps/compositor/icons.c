// call draw_putpixel at the corresponding screen position.
// ============================================================================
void icon_draw(icon_id_t id, int32_t x, int32_t y, uint32_t color)
{
    if ((int)id < 0 || id >= ICON_COUNT)
        return;

    const icon_t *ic = &icons[id];
    const uint8_t *data = ic->data;
    int row, col;

    for (row = 0; row < ic->height; row++) {
        // 3 bytes cover 24 columns: byte 0 = cols 0-7, byte 1 = cols 8-15,
        // byte 2 = cols 16-23. MSB of each byte is the leftmost column.
        uint8_t b0 = data[row * 3 + 0];
        uint8_t b1 = data[row * 3 + 1];
        uint8_t b2 = data[row * 3 + 2];

        for (col = 0; col < 8; col++) {
            if (b0 & (0x80u >> col))
                draw_putpixel(x + col,      y + row, color);
            if (b1 & (0x80u >> col))
                draw_putpixel(x + col + 8,  y + row, color);
            if (b2 & (0x80u >> col))
                draw_putpixel(x + col + 16, y + row, color);
        }
    }
}

// ============================================================================
// icon_draw_scaled: render an icon scaled to size x size pixels.
//
// Uses fixed-point 8.8 arithmetic to map each destination pixel back to a
// source bitmap coordinate, then tests the corresponding source bit. No
// heap allocation; intermediate values are kept as 16.8 fixed-point integers
// (stored in int32_t) to stay freestanding.
//
// src_col = (dst_col * (src_w << 8)) / (size << 8)  simplified:
//         = (dst_col * src_w) / size   (integer division)
// Similarly for rows.
// ============================================================================
void icon_draw_scaled(icon_id_t id, int32_t x, int32_t y, int32_t size, uint32_t color)
{
    if ((int)id < 0 || id >= ICON_COUNT)
        return;
    if (size <= 0)
        return;

    const icon_t *ic = &icons[id];
    const uint8_t *data = ic->data;
    int src_w = ic->width;   // 24
    int src_h = ic->height;  // 24
    int32_t dst_row, dst_col;

    // Scale factors in 8.8 fixed point: step per destination pixel.
    // step_x = (src_w << 8) / size
    // step_y = (src_h << 8) / size
    int32_t step_x = (src_w << 8) / size;
    int32_t step_y = (src_h << 8) / size;

    int32_t src_y_fp = 0;
    for (dst_row = 0; dst_row < size; dst_row++, src_y_fp += step_y) {
        int src_row = src_y_fp >> 8;
        if (src_row >= src_h)
            src_row = src_h - 1;

        // Cache the three source bytes for this row.
        uint8_t b0 = data[src_row * 3 + 0];
        uint8_t b1 = data[src_row * 3 + 1];
        uint8_t b2 = data[src_row * 3 + 2];

        int32_t src_x_fp = 0;
        for (dst_col = 0; dst_col < size; dst_col++, src_x_fp += step_x) {
            int src_col = src_x_fp >> 8;
            if (src_col >= src_w)
                src_col = src_w - 1;

            // Determine which byte and which bit within that byte.
            int byte_idx = src_col / 8;
            int bit_idx  = 7 - (src_col % 8);  // MSB = leftmost column

            uint8_t byte_val;
            if (byte_idx == 0)      byte_val = b0;
            else if (byte_idx == 1) byte_val = b1;
            else                    byte_val = b2;

            if (byte_val & (1u << bit_idx))
                draw_putpixel(x + dst_col, y + dst_row, color);
        }
    }
}
