// Get font glyph for a character
const uint8_t *font_get_glyph(char c) {
    unsigned char uc = (unsigned char)c;
    if (uc > 127) {
        return font_data[0];  // Return blank for invalid chars
    }
    return font_data[uc];
}

// Draw character at half scale (4x8)
void draw_char_small(int32_t x, int32_t y, char c, uint32_t color) {
    if ((unsigned char)c > 127) c = '?';
    
    const uint8_t *glyph = font_data[(int)c];
    
    // Sample every other row and every other column
    for (int row = 0; row < 8; row++) {
        uint8_t src_row = glyph[row * 2];  // Sample rows 0,2,4,6,8,10,12,14
        for (int col = 0; col < 4; col++) {
            // Sample columns 0,2,4,6 (bits 7,5,3,1)
            if (src_row & (0x80 >> (col * 2))) {
                fb_put_pixel(x + col, y + row, color);
            }
        }
    }
}

// Draw string at half scale
void draw_string_small(int32_t x, int32_t y, const char *str, uint32_t color) {
    while (*str) {
        draw_char_small(x, y, *str, color);
        x += FONT_SMALL_WIDTH;
        str++;
    }
}
