const icon_t *icon_get(icon_id_t id) {
    if (id >= ICON_COUNT) return NULL;
    return &icons[id];
}

void icon_draw(icon_id_t id, int x, int y, uint32_t color) {
    const icon_t *icon = icon_get(id);
    if (!icon || !icon->data) return;

    int bytes_per_row = (icon->width + 7) / 8;

    for (int row = 0; row < icon->height; row++) {
        for (int col = 0; col < icon->width; col++) {
            int byte_idx = row * bytes_per_row + col / 8;
            int bit_idx = 7 - (col % 8);

            if (icon->data[byte_idx] & (1 << bit_idx)) {
                fb_put_pixel(x + col, y + row, color);
            }
        }
    }
}

void icon_draw_scaled(icon_id_t id, int x, int y, int size, uint32_t color) {
    const icon_t *icon = icon_get(id);
    if (!icon || !icon->data || size <= 0) return;

    int bytes_per_row = (icon->width + 7) / 8;

    // Calculate scale factor (fixed point 8.8)
    int scale_x = (icon->width << 8) / size;
    int scale_y = (icon->height << 8) / size;

    for (int dy = 0; dy < size; dy++) {
        int src_y = (dy * scale_y) >> 8;
        if (src_y >= icon->height) src_y = icon->height - 1;

        for (int dx = 0; dx < size; dx++) {
            int src_x = (dx * scale_x) >> 8;
            if (src_x >= icon->width) src_x = icon->width - 1;

            int byte_idx = src_y * bytes_per_row + src_x / 8;
            int bit_idx = 7 - (src_x % 8);

            if (icon->data[byte_idx] & (1 << bit_idx)) {
                fb_put_pixel(x + dx, y + dy, color);
            }
        }
    }
}
